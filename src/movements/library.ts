/** Index movements by ID for fast lookup */
const movementIndex = new Map<string, Movement>();
for (const m of MOVEMENT_LIBRARY) {
  movementIndex.set(m.id, m);
}

export function getMovement(id: string): Movement | undefined {
  return movementIndex.get(id);
}

export function getMovementOrThrow(id: string): Movement {
  const m = movementIndex.get(id);
  if (!m) throw new Error(`Movement not found: ${id}`);
  return m;
}

export function getAllMovements(): Movement[] {
  return [...MOVEMENT_LIBRARY];
}

export function getMovementsByModality(modality: Modality): Movement[] {
  return MOVEMENT_LIBRARY.filter((m) => m.modality === modality);
}

export function getMovementsByMuscleGroup(group: MuscleGroup): Movement[] {
  return MOVEMENT_LIBRARY.filter((m) => m.muscleGroups.includes(group));
}

export function getMovementsByEquipment(equipment: Equipment[]): Movement[] {
  const available = new Set(equipment);
  return MOVEMENT_LIBRARY.filter((m) =>
    m.equipment.every((e) => e === Equipment.None || available.has(e))
  );
}
